export const getAllJobs = async (req, res) => {
  try {
    //req.query.keyword -> /api/jobs?keyword=software to jo software keyword hoga usse match krne wale job title ya description ko return krna hai
    const keyword = req.query.keyword || "";
    const query = {
      $or: [
        { title: { $regex: keyword, $options: "i" } },
        { description: { $regex: keyword, $options: "i" } },
      ],
    };
    const jobs = await Job.find(query)
      .populate({
        path: "company",
      })
      .sort({ createdAt: -1 });
    if (!jobs) {
      return res.status(404).json({
        message: "Jobs not found.",
        success: false,
      });
    }
    // .populate({
    //     path: "company",
    //   })  ya populate company ka under ja kr detials dega job ke sath kui ki hm jobpost krte time company ka id de rhe hote h aur company:{
    //  type: mongoose.Schema.Types.ObjectId,
    //  ref: "Company"
    // }  ya job model hota hai
    return res.status(200).json({
      jobs,
      success: true,
    });
  } catch (error) {
    console.log(error);
  }
};
